        String digita = jTextField1.getText();
        if (digita.equals("")) {
            JOptionPane.showMessageDialog(this, "Digite algo para pesquisar, por favor!");
        } else {
            String selecionado = jComboBox1.getSelectedItem().toString();
            String curso = jComboBox2.getSelectedItem().toString();
            tabela.addColumn("<html><b>CÓDIGO</b></html>");
            tabela.addColumn("<html><b>TAMANHO</b></html>");
            tabela.addColumn("<html><b>COR</b></html>");
            tabela.addColumn("<html><b>QUANTIDADE</b></html>");

            String res = func.listCamiseta("SELECT * FROM camiseta WHERE tamanho LIKE \"%" + digita + "%\" OR qtd LIKE \"%" + digita + "%\" OR cod LIKE \"%" + digita + "%\" OR cor LIKE \"%" + digita + "%\";");
            System.out.println(res);
            String partes[] = res.split("&");
            int i = 0;
            int k = 0;

            try {
                for (i = 0; i < partes.length; i++) {

                    String partes_partes[] = partes[i].split(";");
                    String[] tudo = {partes_partes[0], partes_partes[3], partes_partes[4], partes_partes[5]};
                    tabela.addRow(tudo);

                }
            } catch (Exception e) {
                System.out.println(e);
            }

            jTextField1.setText("");
            jTable1.setModel(tabela);
            int total = jTable1.getRowCount();
            int k2 = 0;
            int total2 = 0;
            while (k2 < total) {
                total2 += Integer.parseInt("" + tabela.getValueAt(k2, 3));
                k2++;
            }
            jLabel3.setText("" + total2);
            verificar_obs();
        }
