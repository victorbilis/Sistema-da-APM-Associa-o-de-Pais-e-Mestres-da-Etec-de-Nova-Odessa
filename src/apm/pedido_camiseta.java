    public void csv() {
        String colunas = "";
        int i = 0;
        int qtd_colunas = jTable1.getColumnCount();
        while (i < qtd_colunas) {
            colunas += jTable1.getColumnName(i) + ";";
            i++;
        }
        String linhas = "";
        i = 0;
        int k = 0;
        while (i < jTable1.getRowCount()) {
            k = 0;
            while (k < qtd_colunas) {
                linhas += jTable1.getValueAt(i, k) + ";";
                k++;
            }
            linhas += "\n";
            i++;
        }
        try {
            FileOutputStream fos = new FileOutputStream("Pedido de camiseta.csv");
            OutputStreamWriter writer = new OutputStreamWriter(fos, "ISO-8859-1");
            writer.write(colunas + "\n" + linhas);
            writer.close();

        } catch (Exception e) {
            System.out.println(e);
        }

    }

